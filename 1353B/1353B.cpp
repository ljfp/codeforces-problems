void solve() {
	vsi a, b;
	queue<si> qa, qb;
	si n, k;

	cin >> n >> k;

	for (si i = 0; i < n; i++) {
		si x = 0;
		cin >> x;
		a.push_back(x);
	}
	sort(a.begin(), a.end(), less{});

	for (si i = 0; i < n; i++) {
		si x = 0;
		cin >> x;
		b.push_back(x);
	}
	sort(b.begin(), b.end(), greater{});

	int r = 0;
	for (si i = 0; i < n; i++) {
		if (i < k && a[i] < b[i]) {
			a[i] = b[i];
		}
		r += a[i];
	}

	cout << r << "\n";
}

